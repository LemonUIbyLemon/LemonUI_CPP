#pragma once

namespace LemonUI
{
	/// <summary>
	/// The alignment of the element to draw.
	/// </summary>
	enum class Alignment : UINT8
	{
		/// <summary>
		/// Aligns the element to the Center.
		/// </summary>
		Center = 0,
		/// <summary>
		/// Aligns the element to the Left.
		/// </summary>
		Left = 1,
		/// <summary>
		/// Aligns the element to the RIght.
		/// </summary>
		Right = 2
	};
}
